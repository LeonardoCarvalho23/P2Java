    private void limparCampos(){
        tfNome.setText("");
        tfDocumento.setText("");
        tfEmail.setText("");
        tfTelefone.setText("");
        cbAndar.setSelectedIndex(-1);
        cbSala.setSelectedIndex(-1);
        tfHorario.setText("");
                
    }
    private void cadastrarVisita() throws ClassNotFoundException, SQLException{
        visita = new VisitaAgendada();
        ControleVisita controleVisita = new ControleVisita();
        visita.setNome(tfNome.getText());
        visita.setDocumento(tfDocumento.getText());
        visita.setEmail(tfEmail.getText());
        visita.setTelefone(tfTelefone.getText());
        visita.setAndar((String)cbAndar.getSelectedItem());
        visita.setSala((String)cbSala.getSelectedItem());
        visita.setHorario(tfHorario.getText());
        controleVisita.inserirVisita(visita);
        
           limparCampos();
    }
    private void buscarVisita() throws SQLException, ClassNotFoundException{
        sucessoNaBusca = false;
        ControleVisita controleVisita = new ControleVisita();
        ResultSet rs;
        
        rs = controleVisita.buscarVisita(tfDocumento.getText());
        if(rs.first()){
            tfNome.setText(rs.getString(1));
                    tfEmail.setText(rs.getString(2));
                    tfTelefone.setText(rs.getString(3));
                    cbAndar.addItem(rs.getString(6));
                    cbAndar.setSelectedItem(rs.getString(6));
                    cbSala.addItem(rs.getString(6));
                    cbSala.setSelectedItem(rs.getString(6));
                    tfHorario.setText(rs.getString(6));
                    sucessoNaBusca = true;
            
        }
              
    }
    private void alterarVisita() throws ClassNotFoundException, SQLException{
        
        visita.setNome(tfNome.getText());
        ControleVisita controleVisita = new ControleVisita();
        
        visita.setDocumento(tfDocumento.getText());
        visita.setEmail(tfEmail.getText());
        visita.setTelefone(tfTelefone.getText());
        visita.setAndar((String)cbAndar.getSelectedItem());
        visita.setSala((String)cbSala.getSelectedItem());
        visita.setHorario(tfHorario.getText());
        controleVisita.alterarVisita(visita, antigoDocumento);
        
    }
    private void excluirVisita() throws ClassNotFoundException, SQLException{
        ControleVisita controleVisita = new ControleVisita();
        controleVisita.excluirVisita(tfDocumento.getText());
    }

