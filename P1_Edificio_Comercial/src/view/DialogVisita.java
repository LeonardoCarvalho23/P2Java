    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
        int resp;
        if(rbCrudVisitaCadastrar.isSelected()){
            try {
                cadastrarVisita();
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(DialogVisita.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(rbCrudVisitaBuscar.isSelected()){
            try {
                buscarVisita();
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(DialogVisita.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(rbCrudVisitaAlterar.isSelected() && btOk.getText().equals("Buscar")){
            try {
                buscarVisita();
                if (sucessoNaBusca){
                antigoDocumento = tfDocumento.getText();
                btOk.setText("Alterar");
                liberarCampos();
                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(DialogVisita.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(rbCrudVisitaAlterar.isSelected() && btOk.getText().equals("Alterar")){
            try {
                alterarVisita();
                limparCampos();
                bloquearCampos();
                tfDocumento.setEnabled(true);
                btOk.setText("Buscar");
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(DialogVisita.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        else if(rbCrudVisitaExcluir.isSelected()){
            try {
                buscarVisita();
                if(sucessoNaBusca){
                    resp = JOptionPane.showConfirmDialog(null,"Confirma a exclusão", "Exclusão",JOptionPane.YES_NO_OPTION);
                        if(resp == JOptionPane.YES_OPTION){
                        excluirVisita();
                        limparCampos();
                }
            }
                
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(DialogVisita.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione alguma das opções!");
        }
    }//GEN-LAST:event_btOkActionPerformed

